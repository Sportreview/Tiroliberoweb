<p>La prima volta della <strong>MyGlass Carmagnola</strong> ha come vittima il <strong>Videoton Crema</strong>. I lombardi, forti fino adesso di un percorso netto, incappano nella prima sconfitta stagionale. Sono tanti i meriti dei padroni di casa, abili ad ingigantire le distrazioni dei ragazzi di <strong>Uccheddu</strong>. Vedi per esempio il sinistro malefico di <strong>Licciardi</strong> che, dopo appena diciotto secondi, sfrutta a pieno un blocco su calcio d&#8217;angolo. I gialloneri cavalcano l&#8217;onda dell&#8217;inerzia della gara, anche perché i &#8220;vidi&#8221; faticano a prendere le misure a <strong>Giuliano</strong> e compagni. Palla avanti e palla indietro, pronti ad attaccare lo spazio, la MyGlass Carmagnola ha il pallino del mano in gioco ma pecca in concretezza. <strong>Lastra</strong> si dimostra un arrivo azzeccato, in quanto sul laterale crea spesso la superiorità numerica. Su una di queste apparecchia per <strong>Bussetti</strong> che, in una selva di gambe, tocca quel che basta per raddoppiare. <strong>Villa</strong> cerca di creare qualche scompiglio dalle parti di <strong>Zimmari</strong>, ma la sua fisicità non può esplodere nelle metrature del Carmasport.</p>
