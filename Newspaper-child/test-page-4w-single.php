<p>A campi invertiti, è ancora un&#8217;amnesia degli ospiti ad essere punita: sugli sviluppi di un calcio di punizione, Giuliano cerca e trova la porta: 3-0. La MyGlass Elledì Carmagnola vola sulle ali dell&#8217;entusiasmo, anche se in questa disciplina è vietato abbassare la guardia. Così <strong>Porceddu</strong> batte un incolpevole <strong>Cafagna</strong> con un tiro chirurgico all&#8217;incrocio. Il Videoton alza la pressione, ma un&#8217;azione da manuale del futsal, porta <strong>Solavagione</strong> ad allungare ancora nel punteggio: sinistro magico di Licciardi a cambiare lato per <strong>Polimeni</strong> che con lucidità offre un assist perfetto al pivot carmagnolese doc. Il Crema però ha dalla sua parte il cronometro, per iniziare l&#8217;operazione rimonta, ma quando Porceddu calcia fuori un tiro libero, tutto diventa tremendamente più difficile. Il portiere di movimento, è l&#8217;ultima mossa disperata di Uccheddu. I frutti sperati non arrivano visto che Licciardi, <strong>Oanea</strong> e <strong>Novelli</strong> firmano il tabellino dei marcatori, centrando i tre legni. In mezzo c&#8217;è il sigillo di un volenteroso Villa e della new entry <strong>Abelini</strong>, abili ad avvicinarsi ai padroni di casa fino al 5-3, per poi crollare nel finale.</p>
